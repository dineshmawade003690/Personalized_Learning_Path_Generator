    attachPageSpecificHandlers() {
        const profileForm = document.getElementById("profileForm");
        if (profileForm) {
            profileForm.addEventListener("submit", App.handleProfileUpdate.bind(App));
        }

        const dashboardName = document.getElementById("dashboardName");
        if (dashboardName) {
            this.populateDashboard();
        }

        this.populateProfileForm();
    },

    populateProfileForm() {
        const currentUser = App.getStoredUser();
        const usernameInput = document.getElementById("profileUsername");
        const nameInput = document.getElementById("profileName");
        const emailInput = document.getElementById("profileEmail");
        const roleInput = document.getElementById("profileRole");
        const interestsInput = document.getElementById("profileInterests");

        if (!currentUser) {
            return;
        }

        if (usernameInput) usernameInput.value = currentUser.username || currentUser.name || "";
        if (nameInput) nameInput.value = currentUser.name || "";
        if (emailInput) emailInput.value = currentUser.email || "";
        if (roleInput) roleInput.value = currentUser.role || "Learning Explorer";
        if (interestsInput) interestsInput.value = (currentUser.interests || []).join(", ");
    },

    populateDashboard() {
        const currentUser = App.getStoredUser();
        const dashboardName = document.getElementById("dashboardName");
        const dashboardRole = document.getElementById("dashboardRole");
        const dashboardEmail = document.getElementById("dashboardEmail");
        const dashboardInterests = document.getElementById("dashboardInterests");
        const dashboardAvatar = document.getElementById("dashboardAvatar");

        if (!currentUser) {
            return;
        }

        if (dashboardName) dashboardName.textContent = currentUser.name;
        if (dashboardRole) dashboardRole.textContent = currentUser.role || "Learning Explorer";
        if (dashboardEmail) dashboardEmail.textContent = currentUser.email;
        if (dashboardInterests) dashboardInterests.textContent = (currentUser.interests || []).join(", ");
        if (dashboardAvatar) {
            dashboardAvatar.textContent = currentUser.name ? currentUser.name.charAt(0).toUpperCase() : "U";
        }
    },

    updateFileInputLabel(input, statusElement) {
        if (!input || !statusElement) {
            return;
        }

        const fileName = input.files && input.files[0] ? input.files[0].name : "No file chosen";
        statusElement.textContent = fileName.length > 30 ? `${fileName.slice(0, 27)}...` : fileName;
    },

    displayAvatar(src) {
        if (!this.elements.avatarImg) {
            return;
        }

        if (src) {
            this.elements.avatarImg.src = src;
            this.elements.avatarImg.style.display = "block";
            if (this.elements.avatarText) {
                this.elements.avatarText.style.display = "none";
            }
        } else {
            this.elements.avatarImg.src = "avatar.png";
            this.elements.avatarImg.style.display = "none";
            if (this.elements.avatarText) {
                this.elements.avatarText.style.display = "grid";
            }
        }
    },

